#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	int		res;

	i = 0;
	res = 0;
	while (src[res] != '\0')
		res++;
	if (dstsize == 0)
		return (res);
	while (i < dstsize - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (res);
}
